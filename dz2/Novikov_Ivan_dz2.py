# 8. Посчитать, сколько раз встречается определенная цифра в введенной последовательности чисел.
#     Количество вводимых чисел и цифра, которую необходимо посчитать,
#     задаются вводом с клавиатуры.
def amount_numb():
    f = list(map(int, input('Введите числа через пробел ').split()))
    t = int(input('введите чмсло = '))
    s = 0
    for i in f:
        if i == t:
            s = s + 1
    return F'число {t} встречается {s} раз'


# 9. Среди натуральных чисел, которые были введены, найти наибольшее по сумме цифр.
#     Вывести на экран это число и сумму его цифр.
def summ():
    s = list(map(int, input('Введите числа через пробел ').split()))
    f = {}
    for i in s:
        r = 0
        for j in str(i):
            r = r + int(j)
        f[r] = i
    return max(f.items())


