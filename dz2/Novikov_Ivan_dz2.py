# 1. Написать программу, которая будет складывать, вычитать,
#     умножать или делить два числа. Числа и знак операции вводятся пользователем.
#     После выполнения вычисления программа не должна завершаться,
#     а должна запрашивать новые данные для вычислений.
#     Завершение программы должно выполняться при вводе символа '0'
#     в качестве знака операции.
#     Если пользователь вводит неверный знак
#      (не '0', '+', '-', '*', '/'), то программа должна сообщать
#      ему об ошибке и снова запрашивать знак операции.
#      Также сообщать пользователю о невозможности деления на ноль,
#      если он ввел 0 в качестве делителя.
def calculator(a, b, z):
    if z == '+':
        s = a + b
    elif z == '-':
        s = a - b
    elif z == '*':
        s = a * b
    elif z == '/':
        if b == 0:
            s = 'Делить на ноль нельзя'
        else:
            s = a / b
    else:
        s = 'Введен не керектный знак'
    return s

# 2. Посчитать четные и нечетные цифры введенного натурального числа.
#     Например, если введено число 34560, то у него 3 четные цифры
#     (4, 6 и 0) и 2 нечетные (3 и 5).

def even_odd(z):
    e = 0
    o = 0
    s = ''
    for i in z:
        if int(i) % 2 == 1:
            e = e + 1
        else:
            o = o + 1
    s = F'четныйх = {o}, нечетных {e}'
    return s

# 3. Сформировать из введенного числа обратное по порядку входящих в него цифр и вывести на экран.
#     Например, если введено число 3486, то надо вывести число 6843.
def coup(z):
    e = ''
    for i in reversed(z):
        e = e + str(i)
    return e

# 4. Найти сумму n элементов следующего ряда чисел: 1 -0.5 0.25 -0.125 ...
#     Количество элементов (n) вводится с клавиатуры.
def progression(n):
    s = 1
    n = int(n)
    for i in range(1, n + 1):
        # print(i)
        if i % 2 == 1:
            # print(i%2)
            s = s - (1 / (2 ** i))
        else:
            s = s + (1 / (2 ** i))
    return s

# 5. Вывести на экран коды и символы таблицы ASCII, начиная с символа под номером 32
#     и заканчивая 127-м включительно. Вывод выполнить в табличной форме:
#     по десять пар "код-символ" в каждой строке.
def ascii():
    s = []
    for i in range(32, 128):
        s.append(F'{i}={chr(i)}')
    return s

